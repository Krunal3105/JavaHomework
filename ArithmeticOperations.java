package Homework;

public class ArithmeticOperations<a> {

    public static void main(String[] args) {
        int A = 50;
        int B = 70;
        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);


        int a = 25;
        boolean b = true;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(b);
    }
}


